public class dairehesapı {
    public static void main(String[] args) {
        double r = 5.5 ;
        double pi = 3.14;

        // Alan hesapla
        double alan = pi*r*r;

        // Cevre Hesapla
        double cevre = 2*pi*r;

        // Sonucları hesapla
        System.out.println("yariçap: " + r);
        System.out.println("alan: " + alan);
        System.out.println("cevre: "  +cevre);
    }
}
